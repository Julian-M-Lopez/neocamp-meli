package com.meli.calculator.model;

public class Calculator {

    public int sumar(int i, int i1) {
        return i + i1;
    }

    public int restar(int i, int i1) {
        return i - i1;
    }

    public int multiplicar(int i, int i1) {
        return i * i1;
    }

    public double dividir(int i, int i1) {
        return i/i1;
    }
}
